package clases;

public class Estadistica {
    private String nombre;
    private String apellido;
    private double promedio_peso;
    private double promedio_altura;
    private double promedio_asistencia;
    private String estado_plan;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public double getPromedio_peso() {
        return promedio_peso;
    }

    public void setPromedio_peso(double promedio_peso) {
        this.promedio_peso = promedio_peso;
    }

    public double getPromedio_altura() {
        return promedio_altura;
    }

    public void setPromedio_altura(double promedio_altura) {
        this.promedio_altura = promedio_altura;
    }

    public double getPromedio_asistencia() {
        return promedio_asistencia;
    }

    public void setPromedio_asistencia(double promedio_asistencia) {
        this.promedio_asistencia = promedio_asistencia;
    }

    public String getEstado_plan() {
        return estado_plan;
    }

    public void setEstado_plan(String estado_plan) {
        this.estado_plan = estado_plan;
    }


}
